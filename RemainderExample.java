class RemainderExample
{
	public static void main (String[] args )
	{
	int qoutient, remainder;

	qoutient = 17 / 3;
	remainder = 17 % 3;

	System.out.println("The qoutient : " + qoutient );
	System.out.println("The remainder: " + remainder );
	System.out.println("The original : " + (qoutient*3 + remainder));
	}
}
